
public class Address {
	String Country;
	String State;
	String City;
	String Street;
	int ZIP;

	public Address(String Country, String State, String City, String Street,
			int ZIP) {
		this.Country = Country;
		this.State = State;
		this.City = City;
		this.Street = Street;
		this.ZIP = ZIP;
	}
}
